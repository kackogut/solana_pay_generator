/// Spacings follow Material guidelines for colors, with predefinied values
/// for [medium], [small] and [large] spacings and percentage variants of it.
/// So for example [medium_150] means 150% of [medium] 16 * 150% = 24
class Spacing {
  static const small_50 = 4.0;
  static const small_100 = 8.0;

  static const medium_100 = 16.0;
  static const medium_150 = 24.0;

  static const large_100 = 32.0;
}
